"""PyTorch dataset wrappers for the processed FOREX tensors."""

from __future__ import annotations

from pathlib import Path
from typing import Any

import numpy as np
import torch
from torch.utils.data import Dataset

from src.utils import read_json


class ForexPortfolioDataset(Dataset[dict[str, Any]]):
    """Dataset of supervised FOREX portfolio allocation samples."""

    def __init__(
        self,
        processed_dir: str | Path,
        split: str = "train",
        date_start: str | None = None,
        date_end: str | None = None,
    ) -> None:
        """Load tensors for one chronological split.

        Args:
            processed_dir: Directory generated by ``src.data.transform``.
            split: One of ``train``, ``val``, ``test``, or ``all``.
            date_start: Optional start date, inclusive, in ISO format.
            date_end: Optional end date, exclusive, in ISO format.
        """
        self.processed_dir: Path = Path(processed_dir)
        self.metadata: dict[str, Any] = read_json(
            path=self.processed_dir / "metadata.json"
        )
        self.split: str = split
        self.portfolio_features: np.ndarray = np.load(
            file=self.processed_dir / "portfolio_features.npy"
        )
        self.portfolio_raw_returns: np.ndarray = np.load(
            file=self.processed_dir / "portfolio_raw_returns.npy"
        )
        self.next_log_returns: np.ndarray = np.load(
            file=self.processed_dir / "next_log_returns.npy"
        )
        self.dates: np.ndarray = np.load(file=self.processed_dir / "dates.npy")
        self.node_inputs: dict[str, np.ndarray] = {
            "portfolio_signal": self.portfolio_features
        }
        nodes_dir: Path = self.processed_dir / "nodes"
        for node_name in self.metadata["node_names"]:
            if node_name == "portfolio_signal":
                continue
            node_path: Path = nodes_dir / f"{node_name}.npy"
            if node_path.exists():
                self.node_inputs[node_name] = np.load(file=node_path)

        if date_start is not None or date_end is not None:
            self.indices = self._resolve_date_indices(
                date_start=date_start,
                date_end=date_end,
            )
        else:
            start, end = self._resolve_split_bounds(split=split)
            self.indices = np.arange(start=start, stop=end, dtype=np.int64)

    def _resolve_split_bounds(self, split: str) -> tuple[int, int]:
        """Resolve split bounds from metadata.

        Args:
            split: Requested split name.

        Returns:
            Start-inclusive and end-exclusive sample indices.
        """
        if split == "all":
            return 0, int(self.next_log_returns.shape[0])
        splits: dict[str, list[int]] = self.metadata["splits"]
        if split not in splits:
            raise KeyError(
                f"Unknown split {split!r}; available splits: {sorted(splits)}."
            )
        start, end = splits[split]
        return int(start), int(end)

    def _resolve_date_indices(
        self,
        date_start: str | None,
        date_end: str | None,
    ) -> np.ndarray:
        """Resolve sample indices from an inclusive/exclusive date window.

        Args:
            date_start: Start date, inclusive. ``None`` means dataset start.
            date_end: End date, exclusive. ``None`` means dataset end.

        Returns:
            Sample indices whose dates are inside the requested window.
        """
        date_values = self.dates.astype(str)
        mask = np.full(shape=date_values.shape, fill_value=True, dtype=bool)
        if date_start is not None:
            mask &= date_values >= str(date_start)
        if date_end is not None:
            mask &= date_values < str(date_end)
        indices = np.flatnonzero(mask).astype(np.int64)
        if indices.size == 0:
            first_date = str(date_values[0]) if date_values.size else "n/a"
            last_date = str(date_values[-1]) if date_values.size else "n/a"
            raise ValueError(
                "No processed samples found for requested evaluation dates "
                f"{date_start!r} -> {date_end!r}. Available range is "
                f"{first_date} -> {last_date}."
            )
        return indices

    def __len__(self) -> int:
        """Return the number of samples in the dataset split.

        Returns:
            Number of split samples.
        """
        return int(self.indices.shape[0])

    def __getitem__(self, index: int) -> dict[str, Any]:
        """Return one supervised allocation sample.

        Args:
            index: Local split index.

        Returns:
            Dictionary consumed by the model training loop.
        """
        sample_idx: int = int(self.indices[index])
        nodes: dict[str, torch.Tensor] = {
            node_name: torch.as_tensor(data=array[sample_idx], dtype=torch.float32)
            for node_name, array in self.node_inputs.items()
        }
        return {
            "portfolio_features": torch.as_tensor(
                data=self.portfolio_features[sample_idx],
                dtype=torch.float32,
            ),
            "portfolio_raw_returns": torch.as_tensor(
                data=self.portfolio_raw_returns[sample_idx],
                dtype=torch.float32,
            ),
            "nodes": nodes,
            "next_log_returns": torch.as_tensor(
                data=self.next_log_returns[sample_idx],
                dtype=torch.float32,
            ),
            "date": str(self.dates[sample_idx]),
        }

    @property
    def num_assets(self) -> int:
        """Return the number of target currencies.

        Returns:
            Number of portfolio assets.
        """
        return int(self.next_log_returns.shape[1])

    @property
    def portfolio_input_dim(self) -> int:
        """Return the portfolio node feature dimension.

        Returns:
            Number of features per portfolio-signal time step.
        """
        return int(self.portfolio_features.shape[-1])

    @property
    def lookback(self) -> int:
        """Return the temporal lookback length.

        Returns:
            Number of time steps per model input.
        """
        return int(self.portfolio_features.shape[1])


def move_batch_to_device(
    batch: dict[str, Any],
    device: torch.device,
) -> dict[str, Any]:
    """Move a collated dataset batch to a PyTorch device.

    Args:
        batch: Batch returned by a PyTorch dataloader.
        device: Target device.

    Returns:
        Batch with tensors moved to the selected device.
    """
    output: dict[str, Any] = {}
    for key, value in batch.items():
        if key == "nodes":
            output[key] = {
                node_name: node_value.to(device=device)
                for node_name, node_value in value.items()
            }
        elif torch.is_tensor(value):
            output[key] = value.to(device=device)
        else:
            output[key] = value
    return output


def infer_node_input_dims(dataset: ForexPortfolioDataset) -> dict[str, int]:
    """Infer heterogeneous node input dimensions from a dataset.

    Args:
        dataset: Loaded FOREX portfolio dataset.

    Returns:
        Mapping from node name to feature dimension.
    """
    return {
        node_name: int(array.shape[-1])
        for node_name, array in dataset.node_inputs.items()
    }
